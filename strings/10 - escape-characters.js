// Using escape characters in JavaScript

// Escape double quotes within a string
var message1 = "She said, \"Hello!\"";
console.log(message1);

// Escape single quotes within a string
var message2 = 'He said, \'How are you?\'';
console.log(message2);

// Insert a newline character
var message3 = "First line\nSecond line";
console.log(message3);

// Insert a tab character
var message4 = "Name:\tJohn";
console.log(message4);

// Insert a backslash character
var message5 = "This is a backslash: \\";
console.log(message5);